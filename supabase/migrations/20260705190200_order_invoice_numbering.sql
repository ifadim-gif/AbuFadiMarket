-- ترقيم "الطلبية بدون فاتورة" كفاتورة حقيقية.
--
-- المبدأ: استلام بضاعة = دَين حقيقي من لحظة الاستلام، حتى قبل وصول ورقة الفاتورة.
-- بدل مسار تتبّعي موازٍ بلا أثر مالي، تُنشأ الطلبية كصفّ فاتورة عادي لكن دون رقم
-- ورقي؛ فيملأ DEFAULT العمودُ الرقمَ تلقائيًا بتسلسل عام NO-##### (لكل الموردين)،
-- ثم يُرحّل محفّز book_invoice_debt الدَّينَ كأي فاتورة. عند وصول الفاتورة الورقية
-- يُعدَّل paper_no فقط عبر شاشة تعديل الفاتورة — وتعديل الرقم وحده (دون تغيّر المبلغ)
-- لا يُكرّر الدَّين (محفّز الدَّين يتجاهله). إن لم يُبدَّل الرقم، يبقى الدَّين مسجَّلًا
-- ومحسوبًا برقم NO-##### طبيعيًا.
--
-- استُخدم DEFAULT على العمود (لا محفّز) ليصبح paper_no اختياريًا في نوع الإدراج:
-- الفاتورة العادية ترسل رقمها فيتجاوز الافتراضي؛ الطلبية تتركه فيُملأ تلقائيًا.
-- استخدام التسلسل مكفول لـ authenticated أصلًا عبر منح التسلسلات في ملف grants
-- (default privileges)، فلا حاجة لمنح إضافي هنا. التسلسل مجرّد عدّاد بلا أثر أمني،
-- وanon ممنوع من إدراج الفواتير عبر RLS بأي حال.

create sequence if not exists invoice_order_seq;

alter table invoices
  alter column paper_no set default 'NO-' || lpad(nextval('invoice_order_seq')::text, 5, '0');
