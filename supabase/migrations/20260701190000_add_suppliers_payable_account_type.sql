-- توسيع account_type بحاوية "ذمم الموردين" (Accounts Payable).
-- ضرورية لتحقيق قانون حفظ الطاقة (القسم 4): سداد مورد هو تدفّق نقدي خارج
-- حاويات المال الفيزيائية الأربع، ولا يوازن دون حاوية "دين" تقابله.
-- ملاحظة PostgreSQL: يجب أن يكون هذا في ترحيل منفصل عن أي استخدام للقيمة
-- الجديدة، لأن ALTER TYPE ... ADD VALUE لا يمكن استخدامها في نفس المعاملة.
alter type account_type add value 'suppliers_payable';
